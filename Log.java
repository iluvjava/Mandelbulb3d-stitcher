package LoggingSystem;

public class Log 
{
	
	
	/**
	 * Log info into the terminal 
	 * @param o
	 */
	public static void println(Object o)
	{
		//if(o == null) System.out.println("NULL");
		System.out.println(o);
	}
	
	public static void print(Object o)
	{
		System.out.print(o);
		
	}
	

}
